package org.opentutorials.javatutorials.variables;

public class Double {

	public static void main(Stringdemo[] args) {
		
		double a;
		
		a = 1.1;
		
		System.out.println(a);
		
		double b = 3.141592;
		System.out.println(a+b);
		
		int c = 1;
		System.out.println(b+c);
		
		char d = 'a';
		System.out.println(b+d);
		
	}

}